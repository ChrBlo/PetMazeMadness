// Maze layout definitions
// 1 = wall
// 0 = path
// 2 = goal
// 3 = explosive wall
// 4 = snack (extra life)
// 5 = secret wall
// 6 = secret snack behind secret wall
// 7 = ghoul trigger

export interface MazeLevel {
  id: number;
  name: string;
  timeLimit: number;
  layout: number[][];
  startPosition: { x: number; y: number };
  isDark?: boolean;
  enemies?: Enemy[];
  stalkers?: StalkerEnemy[];
}

export interface Enemy {
  id: string;
  path: { x: number; y: number }[];
  speed: number;
}

export interface StalkerEnemy {
  id: string;
  triggerCell: { x: number; y: number };
  startCell: { x: number; y: number };
  speed: number;
}

